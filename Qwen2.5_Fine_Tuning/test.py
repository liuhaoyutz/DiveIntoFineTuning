import re

def split_think_answer(text):
    # 使用正则表达式匹配<think>和</think>之间的内容
    think_pattern = re.compile(r'<think>(.*?)</think>', re.DOTALL)
    match = think_pattern.search(text)
    
    if match:
        # 提取思考部分并去除首尾空白
        think_content = match.group(1).strip()
        # 提取回答部分并去除首尾空白
        answer_content = text[match.end():].strip()
        return (think_content, answer_content)
    else:
        # 如果没有匹配到，返回空字符串和原始文本
        return ('', text.strip())

text = '''<think>
好，我现在需要解决这个问题：Betty要买一个新钱包，价格是100美元。她现在只有所需钱的一半，也就是50美元。她的父母给了她15美元，祖父母给的是父母的两倍，也就是30美元。那我先算一下她现在总共有多少钱。

首先，Betty原有的钱是50美元，加上父母给的15美元，总共就是65美元。再加上祖父母给的30美元，总金额变成95美元。钱包价格是100美元，所以还需要补上5美元。这样算下来，答案应该是5美元。

不过，为了确保没有错误，我再检查一下计算过程。原来的钱对半分，也就是100的一半是50没错。父母给的是15，祖父母就是两倍30，加起来没错。总和也没错，总共95，还差5。看来没问题。

还有可能吗？比如有没有算漏的地方或者理解错题目意思？题目里说Betty只有所需钱的一半，也就是50美元，之后加上了来自父母和祖父母的钱，分别15和30。所以计算应该是正确的。
</think>

让我们一步步来解决这个问题：

**问题：**
- 钱包的价格是 $100
- Betty 已经有了一半的钱，即有 $50（因为 100 ÷ 2 = 50）
- 她的父母给了她 $15
- 祖父母给的是父母的两倍，也就是 2 × $15 = $30

**步骤：**
1. **计算Betty已经有的钱：**
   - 已有的钱 = $50
2. **加上父母的钱：**
   -$50 + $15 = $65
3. **再加上祖父母的钱：**
   -$65 + $30 = 总共 $95

**计算需要多少钱：**
- 钱包总价格为 $100，已经筹集到 $95
- 所以需要的金额是 100 - 95 = $5

**答案：**
Betty还需要 $5。'''

think, answer = split_think_answer(text)
print("思考部分：\n", think)
print("\n回答部分：\n", answer)
